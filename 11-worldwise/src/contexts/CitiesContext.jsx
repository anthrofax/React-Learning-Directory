import { useEffect, useContext, createContext, useState } from 'react';

const CitiesContext = createContext();

function CitiesProvider({ children }) {
  const [cities, setCities] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [currentCity, setCurrentCity] = useState({});

  const BASE_URL = 'http://localhost:8000';

  useEffect(function () {
    async function getCities() {
      try {
        setIsLoading(true);
        const res = await fetch(`${BASE_URL}/cities`);
        const data = await res.json();

        setCities(data);
      } catch (err) {
        alert('Data kota tidak tersedia');
      } finally {
        setIsLoading(false);
      }
    }

    getCities();
  }, []);

  async function getCity(id) {
    try {
      setIsLoading(true);
      const res = await fetch(`${BASE_URL}/cities/${id}`);
      const data = await res.json();

      setCurrentCity(data);
    } catch (err) {
      alert('Data mengenai city tersebut tidak tersedia.');
    } finally {
      setIsLoading(false);
    }
  }

  async function addCity(cityData) {
    try {
      setIsLoading(true);
      const res = await fetch(`${BASE_URL}/cities`, {
        method: 'POST',
        body: JSON.stringify(cityData),
        headers: {
          "Content-Type": 'application/json'
        }
      });
      const data = await res.json();

      console.log(data)
      setCities([...cities, data])
      setCurrentCity(data);
    } catch (err) {
      alert('Data city gagal ditambahkan.');
    } finally {
      setIsLoading(false);
    }
  }

  return (
    <CitiesContext.Provider
      value={{
        cities,
        isLoading,
        currentCity,
        getCity,
        addCity
      }}
    >
      {children}
    </CitiesContext.Provider>
  );
}

function useCities() {
  const context = useContext(CitiesContext);

  if (!context)
    throw new Error(
      'Anda mengakses context di luar jangkauan provider, context hanya dapat diakses di dalam Provider component!'
    );
  return context;
}

export { CitiesProvider, useCities };
